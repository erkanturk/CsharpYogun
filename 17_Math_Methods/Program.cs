namespace _17_Math_Methods
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //Matematiksel Hazır methodlar

            Console.WriteLine($"Pi sayısı: {Math.PI}");
            Console.WriteLine($"Karekök alma: {Math.Sqrt(25)}");
            Console.WriteLine($"Kuvvetini alma: {Math.Pow(2, 3)}");
            Console.WriteLine($"Mutlak Değer alma: {Math.Abs(-24.4)}");
            Console.WriteLine($"Max Değerini alma: {Math.Max(20, 60)}");
            Console.WriteLine($"Min Değerini alma: {Math.Min(20, 60)}");
            Console.WriteLine($"Tam Yuvarlama: {Math.Round(3.50)}");
            Console.WriteLine($"Yukarı Yuvarlama: {Math.Ceiling(3.01)}");
            Console.WriteLine($"Aşağı Yuvarlama: {Math.Floor(3.99)}");


        }
    }
}
